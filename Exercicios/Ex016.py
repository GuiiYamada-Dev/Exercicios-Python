num = float(input("Digite um valor real: "))
print(f"O valor digitado foi {num} e a sua porção inteira é {int(num)}.")

#Programa no qual pedimos ao usuário um valor real e, em seguida, mostramos este número e exibimos sua porção inteira, usando a função int().

#Também é possível utilizar a função trunc() importando a biblioteca math.
#Porém, trunc() apenas realiza o truncamento (remover os decimais), enquanto int() faz isso e também realiza conversão de tipo.
